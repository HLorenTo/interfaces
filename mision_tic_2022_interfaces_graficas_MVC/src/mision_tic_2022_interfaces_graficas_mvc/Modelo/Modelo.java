package mision_tic_2022_interfaces_graficas_mvc.Modelo;
public class Modelo {//logica del proyecto
    private int n1,n2,resultado;

    public int getN1() {
        return n1;
    }

    public void setN1(int n1) {
        this.n1 = n1;
    }

    public int getN2() {
        return n2;
    }

    public void setN2(int n2) {
        this.n2 = n2;
    }

    public int getResultado() {
        return n1+n2;
    }

    public void setResultado(int resultado) {
        this.resultado = resultado;
    }
}
    

