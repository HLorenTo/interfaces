    private void btnproyeccionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnproyeccionActionPerformed
        int grupo;
        double matricula, docente, admin,plataforma;
        grupo = (int) jSpinner1.getValue();
        matricula =Double.parseDouble(txtmatricula.getText());
        docente =Double.parseDouble(txtdocencia.getText());
        admin =Double.parseDouble(txtadministracion.getText());
        plataforma =Double.parseDouble(txtplataforma.getText());
        Universidads u1 = new Universidads(matricula, docente, plataforma, admin, grupo);
        txtingresos.setText(String.valueOf(u1.CalcularIngreso()));
        txtcostos.setText(String.valueOf(u1.CalcularEgresos()));
        
        
        
        
    }//GEN-LAST:event_btnproyeccionActionPerformed

